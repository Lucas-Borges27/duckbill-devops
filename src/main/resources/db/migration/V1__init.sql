CREATE TABLE USUARIO (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOME VARCHAR2(100) NOT NULL,
    EMAIL VARCHAR2(120) NOT NULL,
    SENHA VARCHAR2(100) NOT NULL,
    ROLE VARCHAR2(20) NOT NULL,
    CONSTRAINT UK_USUARIO_EMAIL UNIQUE (EMAIL)
);

CREATE TABLE CATEGORIA (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOME VARCHAR2(60) NOT NULL,
    CONSTRAINT UK_CATEGORIA_NOME UNIQUE (NOME)
);

CREATE TABLE ATIVO (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TICKER VARCHAR2(12) NOT NULL,
    TIPO VARCHAR2(20) NOT NULL,
    MOEDA_BASE VARCHAR2(3) NOT NULL,
    CONSTRAINT UK_ATIVO_TICKER UNIQUE (TICKER),
    CONSTRAINT CK_ATIVO_TIPO CHECK (TIPO IN ('STOCK','BOND','FUND'))
);

CREATE TABLE DESPESA (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USUARIO_ID NUMBER NOT NULL,
    CATEGORIA_ID NUMBER NOT NULL,
    VALOR NUMBER(12,2) NOT NULL,
    MOEDA VARCHAR2(3) NOT NULL,
    DATA_COMPRA DATE NOT NULL,
    DESCRICAO VARCHAR2(200),
    CONSTRAINT FK_DESPESA_USUARIO FOREIGN KEY (USUARIO_ID) REFERENCES USUARIO(ID),
    CONSTRAINT FK_DESPESA_CATEGORIA FOREIGN KEY (CATEGORIA_ID) REFERENCES CATEGORIA(ID)
);

CREATE TABLE TRANSACAO_ATIVO (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USUARIO_ID NUMBER NOT NULL,
    ATIVO_ID NUMBER NOT NULL,
    TIPO VARCHAR2(4) NOT NULL,
    QTD NUMBER(12,4) NOT NULL,
    PRECO NUMBER(14,6) NOT NULL,
    DATA_NEGOCIO DATE NOT NULL,
    CONSTRAINT FK_TRANSACAO_USUARIO FOREIGN KEY (USUARIO_ID) REFERENCES USUARIO(ID),
    CONSTRAINT FK_TRANSACAO_ATIVO FOREIGN KEY (ATIVO_ID) REFERENCES ATIVO(ID),
    CONSTRAINT CK_TRANSACAO_TIPO CHECK (TIPO IN ('BUY','SELL'))
);

CREATE TABLE COTACAO_ATIVO (
    ATIVO_ID NUMBER NOT NULL,
    DATA_REF DATE NOT NULL,
    PRECO_FECH NUMBER(14,6) NOT NULL,
    CONSTRAINT PK_COTACAO_ATIVO PRIMARY KEY (ATIVO_ID, DATA_REF),
    CONSTRAINT FK_COTACAO_ATIVO FOREIGN KEY (ATIVO_ID) REFERENCES ATIVO(ID)
);

CREATE TABLE COTACAO_MOEDA (
    MOEDA VARCHAR2(3) NOT NULL,
    DATA_REF DATE NOT NULL,
    VALOR_BRL NUMBER(14,6) NOT NULL,
    CONSTRAINT PK_COTACAO_MOEDA PRIMARY KEY (MOEDA, DATA_REF)
);
