CREATE TABLE META (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USUARIO_ID NUMBER NOT NULL,
    TITULO VARCHAR2(100) NOT NULL,
    DESCRICAO VARCHAR2(200),
    VALOR_OBJETIVO NUMBER(12,2) NOT NULL,
    VALOR_GUARDADO NUMBER(12,2) DEFAULT 0 NOT NULL,
    ICONE VARCHAR2(20) NOT NULL,
    COR_DESTAQUE VARCHAR2(7) NOT NULL,
    PRAZO DATE,
    CONSTRAINT FK_META_USUARIO FOREIGN KEY (USUARIO_ID) REFERENCES USUARIO(ID),
    CONSTRAINT CK_META_ICONE CHECK (ICONE IN ('car','airplane','laptop','piggy-bank'))
);

CREATE TABLE TAREFA_FINANCEIRA (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USUARIO_ID NUMBER NOT NULL,
    TITULO VARCHAR2(100) NOT NULL,
    DESCRICAO VARCHAR2(250),
    VALOR_ESTIMADO NUMBER(12,2),
    DATA_LIMITE DATE NOT NULL,
    NOTIFICAR_EM TIMESTAMP NOT NULL,
    STATUS VARCHAR2(12) DEFAULT 'PENDENTE' NOT NULL,
    CONSTRAINT FK_TAREFA_USUARIO FOREIGN KEY (USUARIO_ID) REFERENCES USUARIO(ID),
    CONSTRAINT CK_TAREFA_STATUS CHECK (STATUS IN ('PENDENTE','CONCLUIDA'))
);

